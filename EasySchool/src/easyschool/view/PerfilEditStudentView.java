    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //editarPerfilAluno
        String newNome = nomeTextField.getText();
        String newSenha = senhaTextField.getText();
        String confirmarSenha = confirmarSenhaTextField.getText();
        String matricula = matriculaTextField.getText();
        String newEmail = emailTextField.getText().toString();
        String newTelefone = telefoneTextField.getText().toString();
        String newCurso = cursoTextField.getText().toString();
        String newPeriodo = periodoTextField.getText().toString();
        if (newNome.isEmpty() || newSenha.isEmpty() || matricula.isEmpty()
                || newEmail.isEmpty() || newTelefone.isEmpty() || confirmarSenha.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
            return;
        }
        Aluno newAluno = new Aluno();
        newAluno.setEmail(newEmail);
        newAluno.setName(newNome);
        newAluno.setTelefone(newTelefone);
        newAluno.setSenha(newSenha);
        newAluno.setMatricula(Integer.parseInt(matricula));
        newAluno.setCurso(newCurso);
        newAluno.setPeriodo(newPeriodo);
        if (AlunoDAO.getInstance().editarPerfilAluno(newAluno)) {
            Session.getInstance().setAluno(newAluno);
            JOptionPane.showMessageDialog(null, "Editado com sucesso!");
            HomePageStudentView homeStudent = new HomePageStudentView();
            homeStudent.setVisible(true);
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(null, "Erro com banco de dados.");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

