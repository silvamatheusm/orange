    private void entrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarButtonActionPerformed
        // TODO add your handling code here:
        String newSenha = senhaTextField.getText();
        String newMatricula = matriculaTextField.getText();
        if (newSenha.isEmpty() || newMatricula.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
            return;
        }

        if (alunoRadioButton.isSelected() == true) {
            Aluno aluno = AlunoDAO.getInstance().loginAluno(newMatricula, newSenha);
            if (aluno != null) {
                Session.getInstance().setAluno(aluno);
                HomePageStudentView homeStudent = new HomePageStudentView();
                homeStudent.setVisible(true);
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, "Matricula ou senha incorreta!");
            }
        } else if (professorRadioButton.isSelected() == true) {
            Professor professor = ProfessorDAO.getInstance().loginProfessor(newMatricula, newSenha);
            if (professor != null) {
                if (professor.getMatricula() == 10) {
                    Administrador adm = new Administrador();
                    HomePageAdmView admView = new HomePageAdmView();
                    admView.setVisible(true);
                    this.setVisible(false);
                } else {
                    Session.getInstance().setProfessor(professor);
                    HomePageProfessorView homeProfessor = new HomePageProfessorView();
                    homeProfessor.setVisible(true);
                    this.setVisible(false);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Matricula ou senha incorreta!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um tipo de conta");
            return;
        }


    }//GEN-LAST:event_entrarButtonActionPerformed

